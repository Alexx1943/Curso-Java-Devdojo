package Parte02Operadores;

public class Aula05Atribuição {

    public static void main(String[] args){

        double bonus =  1000;
        bonus += 1000;
        System.out.println(bonus);
        bonus -= 1000;
        System.out.println(bonus);
        bonus *= 2;
        System.out.println(bonus);
        bonus /= 2;
        System.out.println(bonus);
        bonus %= 2;
        System.out.println(bonus);


        int contador = 0;
        contador += 1000;
        System.out.println(contador);
        contador++;
        System.out.println(contador);
        contador--;
        System.out.println(contador);


    }
}
