package Parte02Operadores;

public class Aula02Relacionais {

    public static void main(String[] args){

        int resto = 21 % 2;
        System.out.println(resto);

        boolean is10MaiorQueVinte = 10 > 20;
        boolean is10MenorQueVinte = 10 < 20;
        boolean is10IgualVinte = 10 == 20;
        boolean is10Igualdez = 10 == 10;
        boolean is10DiferenteDez = 10 != 10;


        System.out.println(is10MaiorQueVinte);
        System.out.println(is10MenorQueVinte);
        System.out.println(is10IgualVinte);
        System.out.println(is10Igualdez);
        System.out.println(is10DiferenteDez);

    }

}
